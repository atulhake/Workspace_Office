package java_progrms;

public class Switch {

	public static void main(String[] args) 
	{
		
		
	
		char ss ='B';
		
		switch (ss)
		{
		case 'A': 
			System.out.println("--");
			break;
			
		case 'B': 
			System.out.println("---");
			break;
		case 'C': 
			System.out.println("----");
			break;
		case 'D': 
			System.out.println("-----");
			break;
		case 'E': 
			System.out.println("-------");
			break;

			default:
			System.out.println("Invalid Grade");   
			
			
		}
		
		
	}

}
